# Herança é a capacidade de uma classe filha derivar ou herdar as características e 
# comportamentos da classe pai  (base)
''' Benefícos:
* Representa relacionamentos do mundo real

* Reutilização de código, adicionar mais recursos a uma classe sem modificá-la

* Natureza transitiva, se classe B herdar classe A, todas subclasses de B herdarão 
automaticamente da classe A

SINTAXE DA HERANÇA

class A:
    pass

class B(A):
    pass
'''

# Herança simples é quando a classe herda apenas uma classe pai

